import { sha256 } from "js-sha256";

export const getTwemoji = (seed: string) => {
	const n = Number.parseInt(sha256(seed).slice(0, 2), 16);
	const emoji = emojiList[(n * (emojiList.length / 256)) | 0];
	const codePoints = Array.from(emoji).map((char) =>
		char.codePointAt(0)?.toString(16),
	);
	return {
		alt: emoji,
		src: `https://cdn.jsdelivr.net/gh/twitter/twemoji@latest/assets/svg/${codePoints}.svg`,
	};
};

